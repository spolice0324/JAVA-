
public class Number {

	public static void main(String[] args) {
		System.out.println(6 + 2);
		System.out.println(6 - 2);
		System.out.println(6 * 2);
		System.out.println(6 / 2); //나누기
		
		System.out.println(Math.PI);
		System.out.println(Math.floor(Math.PI)); //버림
		System.out.println(Math.ceil(Math.PI)); //올림
		
	}

}
